class ModelMeasurements
{
	public static void main(String [] args)
	{
		byte minHeight = 69;
		byte maxHeight = 74;
		byte minBust = 32;
		byte maxBust = 36;
		byte minWaist = 22;
		byte maxWaist = 26;
		byte minHip = 33;
		byte maxHip = 35;
		System.out.println("The standard measurements for female models are :");
		System.out.println("Height : "+minHeight+" to "+maxHeight+" inches");
		System.out.println("Bust size : "+minBust+" to "+maxBust+" inches");
		System.out.println("Waist size : "+minWaist+" to "+maxWaist +" inches");
		System.out.println("Hip size : "+minHip+" to "+maxHip +" inches");
	}
}